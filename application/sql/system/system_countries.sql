DROP TABLE IF EXISTS system_countries CASCADE; -- CASCADE will drop all references to this table

CREATE TABLE system_countries (
    id                  bigserial PRIMARY KEY,
    creation            timestamp WITH TIME ZONE NOT NULL DEFAULT now(),
    
    code                varchar(8) NOT NULL CHECK (code = upper(code)),
    country             varchar(128) NOT NULL,
    
    orderby             real,
    
    updated             timestamp WITH TIME ZONE NOT NULL DEFAULT now(),
    active              bool DEFAULT 't' -- NULL=created but not yet active and not deactivated
);

-- Index on ID is automatically created by PRIMARY KEY
CREATE UNIQUE INDEX system_countries_country_x ON system_countries (country);
CREATE UNIQUE INDEX system_countries_code_x ON system_countries (code);
--CREATE INDEX system_countries_id_code_x ON system_countries (id, code);

